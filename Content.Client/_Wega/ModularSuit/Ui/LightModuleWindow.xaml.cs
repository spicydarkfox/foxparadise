using Content.Client.UserInterface.Controls;
using Content.Shared._Wega.ModularSuit.Ui;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.XAML;

namespace Content.Client._Wega.ModularSuit.Ui;

[GenerateTypedNameReferences]
public sealed partial class LightModuleWindow : FancyWindow
{
    public event Action<Color, bool>? OnUpdate;

    public LightModuleWindow()
    {
        RobustXamlLoader.Load(this);

        ConfirmationButton.OnPressed += _ => Update();
    }

    public void UpdateState(LightModuleBoundUserInterfaceState state)
    {
        ColorSelector.Color = state.LightColor;
        MulticolouredCheckbox.Pressed = state.Multicoloured;
    }

    private void Update()
    {
        OnUpdate?.Invoke(ColorSelector.Color, MulticolouredCheckbox.Pressed);
    }
}
